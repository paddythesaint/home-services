export const DIRECTORY_COUNT = DIRECTORY.reduce((n, c) => n + c.providers.length, 0)

// Providers not already in the network (matched by normalized name).
export function directoryCandidates(existingNames) {
  const have = new Set(existingNames.map((n) => (n || "").trim().toLowerCase()))
  return DIRECTORY.map((cat) => ({
    ...cat,
    providers: cat.providers.filter((p) => !have.has(p.name.trim().toLowerCase())),
  })).filter((cat) => cat.providers.length > 0)
}
